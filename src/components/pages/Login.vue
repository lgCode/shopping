<template>
  <div>
    <van-nav-bar title="用户登录" left-text="登录" left-arrow @click-left="goBack" />
    <div class="register-panel">
      <van-field v-model="userName" label="用户名" icon="clear" placeholder="请输入用户名" required
        :error-message="userNameErrorMsg" @click-icon="userName =''" />
      <van-field type="password" v-model="password" label="密码" placeholder="请输入密码" required
        :error-message="passwordErrorMsg" />
      <div class="register-button">
        <van-button type="primary" size="large" @click="loginAction" :loading="openLoading">登录</van-button>
      </div>
    </div>
  </div>
</template>

<script>
  import axios from 'axios';
  import url from '@/serviceAPI.config.js';
  import {
    Toast
  } from 'vant';
  import {
    resolve
  } from 'q';
  export default {
    data() {
      return {
        userName: '',
        password: '',
        openLoading: false, //是否开启用户注册的Loading状态,防止重复提交
        userNameErrorMsg: '', //当用户名出现错误时的提示信息
        passwordErrorMsg: '', //当密码出现错误时的提示信息
      }
    },
    methods: {
      goBack() {
        this.$router.go(-1);
      },
      loginAction() {
        /* if(this.checkForm()){
          this.axiosRegisterUser();
        } */
        this.checkForm() && this.axiosLoginUser();
      },
      //注册账号
      axiosLoginUser() {
        this.openLoading = true;
        axios({
            url: url.loginUser,
            method: 'post',
            data: {
              userName: this.userName,
              password: this.password
            }
          })
          .then(res => {
            console.log(res);
            if (res.data.code == 200 && res.data.message) {
              new Promise((resolve, reject) => {
                  localStorage.userInfo = {
                    userName: this.userName
                  };
                  setTimeout(() => {
                    resolve()
                  }, 500);
                })
                .then(res => {
                  Toast.success('登录成功');
                  this.$router.push('/');
                })
                .catch( err =>{
                  Toast.fail('登录状态保存失败');
                  console.log(err);
                })

            } else {
              Toast.fail('登录失败');
              this.openLoading = false;
            }
          })
          .catch(err => {
            console.log(err);
            Toast.fail('登录失败');
            this.openLoading = false;
          })
      },
      //*******表单验证方法
      checkForm() {
        let isOk = true;
        if (this.userName.length < 5) {
          this.userNameErrorMsg = "用户名不能少于5位";
          isOk = false;
        } else {
          this.userNameErrorMsg = "";
        }
        if (this.password.length < 6) {
          this.passwordErrorMsg = "密码不能少于6位";
          isOk = false;
        } else {
          this.passwordErrorMsg = "";
        }
        return isOk;
      }
    },
    created(){
      if(localStorage.userInfo){
        Toast.success('你已经登录过');
        this.$router.push('/');
      }
    },
    components: {}
  }

</script>

